def print_section(title: str):
    print(f"\n{Color.BOLD}{Color.CYAN}{'='*50}")
    print(f"  {title}")
    print(f"{'='*50}{Color.RESET}")

def success(msg: str):
    print(f"{Color.GREEN}[+] {msg}{Color.RESET}")

def warning(msg: str):
    print(f"{Color.YELLOW}[!] {msg}{Color.RESET}")

def error(msg: str):
    print(f"{Color.RED}[-] {msg}{Color.RESET}")

def info(msg: str):
    print(f"[*] {msg}")
